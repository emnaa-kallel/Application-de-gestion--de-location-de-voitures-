    /**
     * @param args the command line arguments
     */
    private void chargerLocations() {
    LocationDAO dao = new LocationDAO();
    List<Location> liste = dao.getByCin(cin); // `cin` est l’attribut passé au constructeur

    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(new Object[] {
        "ID Location", "Immatriculation", "Date début", "Date fin", "Date retour", "Mode paiement", "Statut"
    });

    for (Location loc : liste) {
        model.addRow(new Object[] {
            loc.getId(),
            loc.getVoitureId(),
            loc.getDateDebut(),
            loc.getDateFin(),
            loc.getDateRetour(),
            loc.getModePaiement(),
            loc.getStatut()
        });
    }

    historiqueLocations.setModel(model);
}


